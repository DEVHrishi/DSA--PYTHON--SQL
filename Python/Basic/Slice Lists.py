my_list = [1, 2, 3, 4, 5]

print(my_list[2:])  #[3, 4, 5]


my_list = [1, 2, 3, 4, 5]

print(my_list[2:4]) # [3, 4]


my_list = [1, 2, 3, 4, 5]

print(my_list[::2])  # [1, 3, 5]


my_list = [1, 2, 3, 4, 5]

print(my_list[::-2])  # [5, 3, 1]


my_list = [1, 2, 3, 4, 5]

print(my_list[1:4:2])  # [2, 4]